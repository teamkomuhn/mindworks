    <article class="expandable companion color" style="--block-color: #FF7557;">

        <div class="main">
            <header>
                <h1>Honeymoon</h1>
                <p>In the "Honeymoon" phase, community cohesion is at its highest. Altruistic action shifts from risk-taking to caring as prosocial behaviour becomes the norm.</p>
                <div class="meta">
                    <span class="part-index">Phase 4</span>
                    <span class="tag">A few weeks</span>
                    <button class="open companion" type="button"><span>Infographic</span></button>
                </div>
            </header>

            <p>The “Honeymoon” stage usually lasts a few weeks and this is the time when helping the impacted community becomes a common purpose. This help can come from the community itself, from rescuers, or even from bystanders who have not been impacted. Within the impacted communities their social identity as a “disaster community” is now strong, reinforced by the sharing of resources, time, stories and experiences. This builds new forms of cooperation and inclusiveness, leading to the emergence of new norms and narratives. Selfish behaviour still prevails in some individuals but it tends to be seen as going against the norm. If still operational, social media is filled with content documenting these shared experiences, often with a high degree of creativity, humour and empathy, as was seen in the diverse "<a href="https://www.youtube.com/watch?v=tRs1hFoGWRs" title="'Lockdown World' - 'Uptown Girl' adapted by the Marsh Family">lockdown videos</a>" during the first wave of the Covid-19 pandemic.</p>

            <p>The community of crisis survivors experiences a feeling of inclusivity that they had never felt in "normal" times. <b>They become part of an empathetic, inclusive and equal society that they didn’t imagine was possible.</b> This can even lead to their <a href="https://www.penguinrandomhouse.com/books/301070/a-paradise-built-in-hell-by-rebecca-solnit/" title="A Paradise Built in Hell">memories of the crisis being filled with joy and positivity despite the loss or chaos</a> they experienced.</p>

            <p>During this time of high social support and creativity, <b>altruistic initiatives flourish</b>. For instance, during the first Covid-19 wave in Europe people came out to <a href="https://www.bbc.com/news/av/world-europe-51895386" title="Coronavirus: Spain and Italy applaud health workers">applaud frontline health workers</a>, and during the ‘Honeymoon’ phase in India’s first wave there were multiple <a href="https://indianexpress.com/article/express-sunday-eye/citizens-helping-migrants-stranded-coronavirus-lockdown-6412617/" title="How citizens are helping migrants stranded by the coronavirus lockdown">initiatives to support migrant workers</a>. Meanwhile, <a href="https://www.greenpeace.org/philippines/story/10534/how-one-persons-action-can-move-a-community/" title="How one person’s action can move a community: the A.T. Reyes Street Community Pantry">neighbourhood pantries were set up</a> in the Philippines to help the local community during the pandemic. </p>

            <p>The large amounts of attention on the impacted community can also increase the amount of support flowing in from the outside, restoring hope for a fast recovery. It is also a phase when we witness a very strong “<a href="https://www.rocketstartup.co/articles/abundance-and-scarcity-mindset/" title="Abundance and scarcity mindset">abundance mindset</a>” with people believing there are enough resources to share with the community. Meanwhile, participation in local actions provides the feeling of agency and, as a consequence, of regaining control over the crisis itself. During the ’Honeymoon’ phase, <a href="https://sciencemediahub.eu/2021/04/14/a-scientists-opinion-interview-with-professor-john-drury-about-changes-in-social-identity-during-covid-19/" title="A scientist’s opinion: Interview with Professor John Drury about changes in social identity during COVID-19">initiatives can be created that continue throughout the following stages of the crisis</a>, even if the public and media are no longer paying attention.</p>

            <p>However, <b>the surge in altruism and community inclusiveness is often wrongly interpreted as a communal shift in values</b>. It is, in fact, the creation of a temporary ‘disaster identity', which is also supported by a shift from people thinking about future wellbeing to just getting through one day at a time.</p>

            <p>As the impacts of the crisis on day-to-day life diminish, so the importance of the community’s “disaster identity” fades, for example, when the floods recede or the state of emergency ends. In this period, old identities from "normal" times, such as professional, racial or political identities, regain importance. When people experience inequality in the impacted community this temporary identity can fade even quicker, for example, if elites have privileged access to resources, or some people lose their jobs while others don’t. The "Honeymoon" phase relies strongly upon <b>a sense of “<a href="http://people.umass.edu/estaub/The_Roots_of_Goodness.pdf" title="The Roots of Goodness">generalised reciprocity</a>”</b><sup>2</sup>, and so when the community feels this has been violated, their belief in this new "inclusive" society falters.</p>

            <aside class="block sidenote">
                <p><i>Generalised reciprocity</i> is the notion that, if one helps another person, this person is not only more likely to help oneself in return but also more likely to help some other person in need. Thus, by helping others, one contributes to creating a world in which people in general will be helpful to oneself as well as to important people in one’s life.</p>
                <button type="button" class="close hide small">Close</button>
            </aside>

            <p>While it is not common, if social division appears early on in the crisis and a new inclusive social identity cannot be created, communities may skip the ‘Honeymoon’ phase altogether, moving directly to the ‘Disillusionment’ phase. The extent and strength of the ‘Honeymoon ’phase strongly depends on the social capital of the community or the rescue and support teams. If rescue operations are poor, if impacted communities are considered a threat to public control or communities are already strongly polarised and unequal, survival mindsets can kick in.</p>

            <p>As quickly as the ‘Honeymoon’ phase appears, it will fade and in just a few weeks can give way to the ‘Disillusionment’ phase.</p>

        </div>

        <section class="recommendations dark">
            <header>
                <h1><span class="sub-title">Phase 4</span> Recommendations</h1>
                <p>The ‘Honeymoon’ phase offers multiple opportunities for experiences and stories about inclusion, equity, solidarity, agency and compassion. These experiences happen in times of reorientation and are <a href="https://docs.google.com/document/d/1a7QqLh0vnz8gxKb6AeSacJhtVkJvTSnmkrWKZ3u6UiM/edit" title="Defining Moments">powerful moments</a> to trigger a long-term, positive mindset change or for communities to imagine a different future. To maximise this impact changemakers should:</p>
            </header>

            <article>
                <h2>Keep communities together</h2>
                <p>Where possible, impacted communities should stay together, in or close to their homes. This allows for the emergence of a ‘disaster community identity’. If possible, evacuation should be avoided.</p>
            </article>
            <article>
                <h2>Hand over the wheel</h2>
                <p>Communities should be supported to help themselves, taking the responsibility for recovery. They should not be alienated by being portrayed as victims or even villains.</p>
            </article>
            <article>
                <h2>Communicate positive new values</h2>
                <p>Use dynamic and normative messaging to share emerging new values like solidarity, altruism or inclusiveness.</p>
            </article>
            <article>
                <h2>Create rituals and symbols</h2>
                <p>These help to increase the community’s visibility, pride and connection with its new identity.</p>
            </article>
            <article>
                <h2>Avoid amplifying antisocial behaviour</h2>
                <p>Even if you are criticising this behaviour it can feed the frame that it is happening, creating what is known as normative ambivalence.</p>
            </article>
            <article>
                <h2>Ensure longevity</h2>
                <p>Communal initiatives and support structures will emerge spontaneously but try to support them to last through the ‘Disillusionment’ phase.</p>
            </article>

