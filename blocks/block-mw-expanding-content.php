            <h1>From disorientation to post-crisis culture</h1>
            <h2 class="sub-title">The crisis timeline</h2>
        </hgroup>
        <p>During a crisis, the impacted community goes through six so-called "psychological phases". These phases are shaped by individual and collective experiences, alongside the other messages that people are exposed to through communications channels.</p>
    </header>

    <article class="expandable" title="block 0, 0">
        <header>
            <h1><span class="sub-title">Phase 1</span> Warning and Threat</h1>
            <p>The first phase is characterised by fear and uncertainty: there are signs that a crisis is coming, but the magnitude and impact are unknown.</p>
        </header>
        <p>The “Honeymoon” stage usually lasts a few weeks and this is the time when helping the impacted community becomes a common purpose. This help can come from the community itself, from rescuers, or even from bystanders who have not been impacted. Within the impacted communities their social identity as a “disaster community” is now strong, reinforced by the sharing of resources, time, stories and experiences. This builds new forms of cooperation and inclusiveness, leading to the emergence of new norms and narratives. Selfish behaviour still prevails in some individuals but it tends to be seen as going against the norm. If still operational, social media is filled with content documenting these shared experiences, often with a high degree of creativity, humour and empathy, as was seen in the diverse "lockdown videos" during the first wave of the Covid-19 pandemic.</p>

        <p>The community of crisis survivors experiences a feeling of inclusivity that they had never felt in "normal" times. They become part of an empathetic, inclusive and equal society that they didn’t imagine was possible. This can even lead to their memories of the crisis being filled with joy and positivity despite the loss or chaos they experienced.</p>

        <p>During this time of high social support and creativity, altruistic initiatives flourish. For instance, during the first Covid-19 wave in Europe people came out to applaud frontline health workers, and during the ‘Honeymoon’ phase in India’s first wave there were multiple initiatives to support migrant workers. Meanwhile, neighbourhood pantries were set up in the Philippines to help the local community during the pandemic.</p>

        <section class="dark">
