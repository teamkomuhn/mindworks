        <div class="main">
            <header>
                <h1>Heroic</h1>
                <p>During the "Heroic" phase, survivors take risks to rescue and support others and we see the emergence of a new collective social identity moulded by a shared experience of the disaster.</p>
                <div class="meta">
                    <span class="part-index">Phase 3</span>
                    <span class="tag">A few days</span>
                    <button class="open companion" type="button"><span>Infographic</span></button>
                </div>
            </header>

            <p>The ‘Heroic’ phase usually only lasts a few days. Long before emergency forces can reach a disaster area, <a href="https://citeseerx.ist.psu.edu/viewdoc/download?doi=10.1.1.453.5726&rep=rep1&type=pdf" title="Citizen Response to Disasters: a Survey of Literature and Some Practical Implications">neighbours become essential first responders</a>, providing physical and emotional support. They rescue people from rooftops or collapsed buildings, administer first aid or provide shelter and food for those who have lost everything. In the ‘Heroic’ phase, the perception that "we are in this together" emerges. This <a href="https://www.tandfonline.com/doi/abs/10.1080/10463283.2018.1471948?scroll=top&needAccess=true&journalCode=pers20&" title="The role of social identity processes in mass emergency behaviour: An integrative review">new identity</a> creates mutual willingness, expectations and norms within the ‘disaster community’ to help and care for each other. These communities are naturally inclusive - everybody who has suffered from the crisis can form part of it. This new shared identity boosts altruism throughout the impacted community whilst other emotional drivers, such as the desire to regain agency, also motivate people to take more actions to support or rescue others. As these actions are often driven by emotions instead of rational thought, they can lead to people taking unnecessary risks, and be limited in their effectiveness. For example, people may help each other in an epidemic without having the necessary protection gear, or they may limit more strategic responses, as was seen in community organised <a href="https://www.youtube.com/watch?v=ybzlfsNnDSc" title="Indian temple offers drive-through oxygen amid Covid crisis - BBC News">oxygen drive-bys during the Covid-19</a> pandemic in India, which limited the ability to prioritise oxygen access to the most vulnerable.</p>

            <p>In this phase, people are still making sense of the new situation and adjusting their behaviour accordingly. As old norms and narratives are rendered unfit for the present, new norms rapidly emerge. Communities start to make sense of the situation by building on the collective experience and identities are formed in the early hours of the crisis.</p>
        </div>

        <section class="recommendations dark">
            <header>
                <h1><span class="sub-title">Phase 3</span> Recommendations</h1>
                <p>The ‘Heroic’ phase is one of intense emotions, like fear, courage, grief and gratitude. People are looking for orientation from other community members, and values and norms are rapidly emerging from the community’s new shared ‘disaster identity’. This is the time to ensure the development of the right norms and narratives.</p>
            </header>

            <article>
                <h2>Lead by exampler</h2>
                <p>Act within the norms and values you want to see. Help others whenever you can, but also provide space for others to help you. Build agency for others to help themselves and allow for moments of pride.</p>
            </article>
            <article>
                <h2>Share stories of altruistic action and community support</h2>
                <p>When done well, these can help build pride and confidence within the community. They can also inspire and support the spread of altruism, even motivating <a href="https://www.scientificamerican.com/article/a-potential-benefit-to-memories-of-terrorism/" title="A Potential Benefit to Memories of Terrorism">people to take similar actions in future crises</a> or in non-crisis times. Create moments to connect, reflect, and recover.</p>
            </article>
            <article>
                <h2>Offer physical & emotional support</h2>
                <p>Both of these are in high demand during this time.</p>
            </article>
            <article>
                <h2>Empathise and feel</h2>
                <p>Try to be aware of your own feelings and empathise with the feelings of impacted people, even if they differ from your own.</p>
            </article>

