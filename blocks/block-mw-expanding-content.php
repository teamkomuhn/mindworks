        <div class="main">
            <header>
                <h1>Recovery</h1>
                <p>This phase is characterised by a feeling of moving on: individuals and communities begin to assume responsibility for rebuilding their lives, and people adjust to a “new normal”.</p>
                <div class="meta">
                    <span class="part-index">Phase 6</span>
                    <span class="tag">A few years</span>
                    <button class="open companion" type="button"><span>Infographic</span></button>
                </div>
            </header>

            <p>The “Recovery” stage can take years, with all of the emotions, perceptions, narratives and norms that were built up and negotiated during the crisis (<a href="https://mindworkslab.org/thedisruptedmind/scientific-insights/" title="The Disrupted Mind: Scientific Insights">see The Disrupted Mind: Scientific Insights</a>) shaping the years that follow.</p>

            <p>Even though on the surface life might resemble that of pre-disaster times, the crisis will have resulted in significant shifts in the mindsets of the survivors. In the US, the 2008 financial crisis not only created a whole new "<a href="https://www.pbs.org/newshour/show/how-the-2008-economic-crisis-created-a-new-wandering-tribe-of-seasonal-workers" title="How the 2008 economic crisis created a ‘new wandering tribe’ of seasonal workers">wandering tribe</a>" of seasonal workers, but also <a href="https://www.cnbc.com/2018/09/10/how-the-financial-crisis-led-to-the-rise-of-donald-trump.html" title="Secretive cabals, fear of immigrants and the tea party: How the financial crisis led to the rise of Donald Trump">fostered the anti-elite mindset that paved the way for Donald Trump</a> eight years later. In the Covid-19 crisis, some countries may have entered the ‘Recovery’ phase through vaccination, while others continue to experience significant aftershocks, or are still struggling to contain the first ones. This leads to a strong disparity in the stages of disaster between rich and poor nations, locking the latter in the ‘Disillusionment’ phase.</p>

            <p>People's perspective moves from the present to the future, creating room for planning and expectations, but also for failure and disappointment. The long lockdowns witnessed during the Covid-19 pandemic are a good example of the grinding pressure of the ‘Disillusionment’ phase and how it can lead to a significant increase in <a href="https://timesofindia.indiatimes.com/city/guwahati/mental-health-biggest-casualty-in-second-wave/articleshow/83457017.cms" title="Mental health biggest casualty in second wave">mental health problems</a>. Meanwhile, after natural disasters, disillusionment sets in when people realise that rebuilding their community will take much longer than they initially thought, <a href="https://www.cbc.ca/news/world/haiti-earthquake-8-year-anniversary-1.5034685" title="Rubble and rage come together in Haiti to highlight slow pace of disaster recovery">as was the case in Haiti</a> following the 2010 earthquake.</p>

