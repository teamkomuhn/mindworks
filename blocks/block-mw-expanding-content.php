        <div class="main">
            <header>
                <h1>Disillusionment</h1>
                <p>When optimism turns into discouragement and the inclusive disaster community breaks down.</p>
                <div class="meta">
                    <span class="part-index">Phase 5</span>
                    <span class="tag">A few months</span>
                    <button class="open companion" type="button"><span>Infographic</span></button>
                </div>
            </header>

            <p>During this phase, which usually lasts for months, people often perceive the crisis or its aftermath as uncontrollable, losing their sense of agency. <b>Fear, depression, anxiety, anger and frustration</b> increase and people switch from an "abundance mindset" to a "<a href="https://www.rocketstartup.co/articles/abundance-and-scarcity-mindset/" title="Abundance and scarcity mindset">scarcity mindset</a>"<sup>3</sup>. This notion of scarcity can be triggered by a decrease in access to resources, limited freedom, social contact, or just dwindling personal energy due to the enduring stress of the crisis.</p>

            <aside class="block sidenote">
                <p>A <i>scarcity mindset</i> is the belief that there will never be enough, resulting in feelings of fear, stress, and anxiety.</p>
                <button type="button" class="close hide small">Close</button>
            </aside>

            <p>While the "Honeymoon" phase sees an increase in compassion, in the "Disillusionment" phase it tends to decrease as people <b>revert back to traditional identities and smaller ingroups like family and friends</b>. People also start to <a href="https://duanehallock.com/2010/01/27/phases-of-disaster-recovery/" title="Understanding the Four Phases of Disaster Recovery">look for others to blame</a> for their prolonged suffering, and we see an increase in fragmentation and polarisation. By now, people begin to get tired of reading about the crisis and so the media coverage decreases. Meanwhile on social media, while some move from sharing experiences to sharing conspiracy theories, others start to turn their attention away from the crisis in search of distraction.</p>

            <p>People's perspective moves from the present to the future, creating room for planning and expectations, but also for failure and disappointment. The long lockdowns witnessed during the Covid-19 pandemic are a good example of the grinding pressure of the ‘Disillusionment’ phase and how it can lead to a significant increase in <a href="https://timesofindia.indiatimes.com/city/guwahati/mental-health-biggest-casualty-in-second-wave/articleshow/83457017.cms" title="Mental health biggest casualty in second wave">mental health problems</a>. Meanwhile, after natural disasters, disillusionment sets in when people realise that rebuilding their community will take much longer than they initially thought, <a href="https://www.cbc.ca/news/world/haiti-earthquake-8-year-anniversary-1.5034685" title="Rubble and rage come together in Haiti to highlight slow pace of disaster recovery">as was the case in Haiti</a> following the 2010 earthquake.</p>

            <p>This phase can also be prolonged by what are known as triggering events or aftershocks. For example, when the testing of a contaminated site after a nuclear or chemical accident shows that evacuated communities will not be able to return to their homes, or news about new variants of a virus, or delays in vaccination during a pandemic.</p>

            <p>Some aftershocks can be so great that they take the whole community back to the ‘Impact’ phase and through phases two to five again. This could be a repetition of the first initial disaster such as a second seismic shock after an earthquake, or an unexpectedly strong infection wave in an epidemic. Aftershocks can also include other disruptions related to the initial disaster, like an economic crisis or social unrest.</p>

            <p>While this phase is influenced by external factors, it is largely determined by the perceptions and attitudes of the impacted community, comparable to the stages of grief one needs to go through before reaching acceptance. When acceptance is reached, people can move on to the ‘Recovery’ phase.</p>

        </div>

        <section class="recommendations dark">
            <header>
                <h1><span class="sub-title">Phase 5</span> Recommendations</h1>
                <p>During this phase, changemakers should focus first on preserving the progress made in the ‘Honeymoon’ phase: the memories, narratives, norms that could lead to a positive mindset shift. They should also try to get the community to the ‘Recovery’ phase as quickly as possible. Though we cannot prolong the ‘Honeymoon’ phase, with the right interventions we can influence how deep communities fall in this phase.</p>
            </header>

            <article>
                <h2>Prepare people</h2>
                <p>Make communities aware, even during the ‘Honeymoon’ phase, that disillusionment will come. Help people realise they are in this for the long haul, that there are struggles ahead and attention from media, politicians and others will fade.</p>
            </article>
            <article>
                <h2>Encourage conversations</h2>
                <p>Conversations are crucial to make people feel that they are being heard but also to create alignment between the different views and interpretations of the situation that can arise.</p>
            </article>
            <article>
                <h2>Avoid division</h2>
                <p>Avoid patronising, a lack of transparency or highlighting injustices that could divide the community.</p>
            </article>
            <article>
                <h2>Maintain confidence & pride</h2>
                <p>The more communities have developed these values, the more likely they will continue to rely on each other and take responsibility for the future.</p>
            </article>
            <article>
                <h2>Offer help</h2>
                <p>Some members of the community will start to suffer from depression, PTSD or other trauma-related consequences. Organising help for them, without creating stigma is important.</p>
            </article>

