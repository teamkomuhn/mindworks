            <p>In this stage, different reactions and experiences depend on the kind of disaster: some crises have no warning, like a terror attack; others, like typhoons, might have days of warning allowing people to prepare themselves. Meanwhile, in a pandemic like Covid-19, while some people are taking actions to prevent infection, others elsewhere are fighting for their lives. Previous crisis experiences also strongly influence the reaction of the impacted community. For example, some communities that had experienced earlier epidemics like Sars and Mers, such as those in South Korea and Taiwan, used the time wisely, whilst others in Europe and the Americas ignored the real danger.</p>

            <p>The inability to cope with uncertainty and fear can lead to denial, and in this period we often see signs of panic in some people. This can be characterised by a strong "me and my family first attitude", exemplified by hoarding, panic buying, bunkering down or fleeing. The looming crisis can also lead to disorientation – the ability of people to make sense of the situation and know how to behave. People search for answers by watching how others act and so when the media focuses on extreme behaviour like panic buying, this can easily lead to more people doing the same.</p>

            <p>The messages that affected groups are exposed to can also influence the narratives and norms of the crisis to come. In this phase, people build on the previously established social norms to find acceptable new rules for the emergency, such as how to communicate on social media or how to share and support each other (or not). These can be built on already established rules in the community, or on ones established during previous crises.</p>

